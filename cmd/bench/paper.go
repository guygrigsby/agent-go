// paperTables renders the paper's data artifacts from run dirs: a booktabs
// results table and summary macros. The LaTeX \inputs these files, so the
// paper's numbers are generated, never transcribed.
func paperTables(runDirs []string, outDir string) error {
	episodes, err := readEpisodes(runDirs)
	if err != nil {
		return err
	}
	rows := aggregate(episodes)
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return err
	}

	var tb strings.Builder
	tb.WriteString("% GENERATED by `bench paper` — do not edit.\n")
	tb.WriteString("\\begin{tabular}{llrrrrr}\n\\toprule\n")
	tb.WriteString("profile & task & mode & pass@k & median green (s) & capped & resends \\\\\n\\midrule\n")
	for _, r := range rows {
		green := "--"
		if r.MedianGreen > 0 {
			green = fmt.Sprintf("%.0f", r.MedianGreen)
		}
		fmt.Fprintf(&tb, "%s & %s & %s & %d/%d & %s & %d & %d \\\\\n",
			texEscape(r.Profile), texEscape(r.Task), r.Mode, r.Passes, r.N, green, r.Capped, r.Resends)
	}
	tb.WriteString("\\bottomrule\n\\end{tabular}\n")
	if err := os.WriteFile(filepath.Join(outDir, "results_table.tex"), []byte(tb.String()), 0o644); err != nil {
		return err
	}

	var semP, semN, rawP, rawN int
	for _, r := range rows {
		switch r.Mode {
		case "semantic":
			semP += r.Passes
			semN += r.N
		case "raw":
			rawP += r.Passes
			rawN += r.N
		}
	}
	pct := func(p, n int) string {
		if n == 0 {
			return "--"
		}
		return fmt.Sprintf("%.0f\\%%", 100*float64(p)/float64(n))
	}
	var mb strings.Builder
	mb.WriteString("% GENERATED by `bench paper` — do not edit.\n")
	fmt.Fprintf(&mb, "\\newcommand{\\EpisodeCount}{%d}\n", len(episodes))
	fmt.Fprintf(&mb, "\\newcommand{\\SemanticPassRate}{%s}\n", pct(semP, semN))
	fmt.Fprintf(&mb, "\\newcommand{\\RawPassRate}{%s}\n", pct(rawP, rawN))
	fmt.Fprintf(&mb, "\\newcommand{\\SemanticPassCount}{%d/%d}\n", semP, semN)
	fmt.Fprintf(&mb, "\\newcommand{\\RawPassCount}{%d/%d}\n", rawP, rawN)
	if err := os.WriteFile(filepath.Join(outDir, "summary_macros.tex"), []byte(mb.String()), 0o644); err != nil {
		return err
	}
	return paperFigures(rows, outDir)
}

// paperFigures renders the two cross-model bar charts the paper needs: pass@k
// by arm and invalid-intermediate rate by arm, each a pgfplots grouped bar
// over the profiles in the grid. Coordinates are the pooled per-profile,
// per-arm aggregate, so the figures cannot drift from the table above them.
func paperFigures(rows []row, outDir string) error {
	type cell struct{ pass, n, invalid, states int }
	pool := map[string]map[string]*cell{} // profile -> mode -> counts
	var profiles []string
	for _, r := range rows {
		if pool[r.Profile] == nil {
			pool[r.Profile] = map[string]*cell{}
			profiles = append(profiles, r.Profile)
		}
		c := pool[r.Profile][r.Mode]
		if c == nil {
			c = &cell{}
			pool[r.Profile][r.Mode] = c
		}
		c.pass += r.Passes
		c.n += r.N
		c.invalid += r.Invalid
		c.states += r.States
	}
	sort.Strings(profiles)
	// ponytail: symbolic x coords must match \addplot keys verbatim, so the
	// profile names go in unescaped; the grid's names have no _, &, %, or #.
	coords := strings.Join(profiles, ",")
	modes := []string{"raw", "serena", "semantic"}
	fig := func(ylabel string, val func(c *cell) float64) string {
		var b strings.Builder
		b.WriteString("% GENERATED by `bench paper` — do not edit.\n")
		b.WriteString("\\begin{tikzpicture}\n\\begin{axis}[\n")
		b.WriteString("  ybar, bar width=7pt, width=\\linewidth, height=6cm,\n")
		fmt.Fprintf(&b, "  ymin=0, ymax=100, ylabel={%s},\n", ylabel)
		fmt.Fprintf(&b, "  symbolic x coords={%s},\n", coords)
		b.WriteString("  xtick=data, x tick label style={rotate=18,anchor=east,font=\\small},\n")
		b.WriteString("  legend style={at={(0.5,1.04)},anchor=south,legend columns=-1,draw=none},\n")
		b.WriteString("  enlarge x limits=0.35, nodes near coords,\n")
		b.WriteString("  every node near coord/.append style={font=\\tiny,/pgf/number format/precision=0,/pgf/number format/fixed},\n")
		b.WriteString("]\n")
		for _, m := range modes {
			b.WriteString("\\addplot coordinates {")
			for i, p := range profiles {
				c := pool[p][m]
				v := 0.0
				if c != nil {
					v = val(c)
				}
				if i > 0 {
					b.WriteByte(' ')
				}
				fmt.Fprintf(&b, "(%s,%.0f)", p, v)
			}
			b.WriteString("};\n")
		}
		b.WriteString("\\legend{raw, serena, semantic}\n")
		b.WriteString("\\end{axis}\n\\end{tikzpicture}\n")
		return b.String()
	}
	passFig := fig("pass@k (\\%)", func(c *cell) float64 {
		if c.n == 0 {
			return 0
		}
		return 100 * float64(c.pass) / float64(c.n)
	})
	invFig := fig("invalid mid-states (\\%)", func(c *cell) float64 {
		if c.states == 0 {
			return 0
		}
		return 100 * float64(c.invalid) / float64(c.states)
	})
	if err := os.WriteFile(filepath.Join(outDir, "pass_by_arm.tex"), []byte(passFig), 0o644); err != nil {
		return err
	}
	return os.WriteFile(filepath.Join(outDir, "invalid_by_arm.tex"), []byte(invFig), 0o644)
}

func texEscape(s string) string {
	r := strings.NewReplacer("_", "\\_", "&", "\\&", "%", "\\%", "#", "\\#")
	return r.Replace(s)
}
